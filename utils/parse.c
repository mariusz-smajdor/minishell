#include "../minishell.h"

int	words_count(char const *s, char c)
{
	int	i;

	i = 0;
	while (*s)
	{
		while (*s != '\0' && *s == c)
			s++;
		if (*s)
			i++;
		while (*s != '\0' && *s != c)
			s++;
	}
	return (i);
}
